    public class VersificationBook
    {
        public string bookCode;			// Three letter abbreviation for book (English)
        public int[] versesPerChapter;  // Highest verse number in each chapter in this book
        public int versesPresent;              // Total verses in all chapters in versification
        public int versesChecked;              // Verses present in actual translation

        public VersificationBook(string bookTla, int[] vsPerChap)
        {
            bookCode = bookTla;
            versesPerChapter = vsPerChap;
            versesChecked = 0;
            versesPresent = 0;
            foreach (int vcount in versesPerChapter)
            {
                versesPresent += vcount;
            }

        }
    }

