        void CheckVersification(Versification vss)
        {
            if (((vss.booksMissing < missingBookVs) ||  // First: avoid missing books 
                (vss.booksMissing == missingBookVs) && (vss.versesMissingChapter < missingChapVs))  // Got books, but avoid missing chapters
                || ((vss.booksMissing == missingBookVs) && (vss.versesMissingChapter == missingChapVs) && (vss.versesMissing < missingVs))  // Got books and chapters, but avoid missing verses
                || ((vss.booksMissing == missingBookVs) && (vss.versesMissingChapter == missingChapVs) && (vss.versesMissing == missingVs) && (vss.extraVerses() < extraVs)))   // Got all books and chapters, choose smallest extra verse set
            {
                bestVersification = vss.versificationName;
                missingBookVs = vss.booksMissing;
                missingChapVs = vss.versesMissingChapter;
                missingVs = vss.versesMissing;
                extraVs = vss.extraVerses();
            }
           
           /*
            Logit.WriteLine(String.Format("Versification {0}: {1} verses in missing books; {2} verses missing chapter; {3} missing verses; {4} extra verses", vss.versificationName, vss.booksMissing,
                vss.versesMissingChapter, vss.versesMissing, vss.extraVerses()));
           */
        }

        public string GetSwordVersification()
        {
            bestVersification = "NRSVA";
            missingBookVs = NRSVA.booksMissing;
            missingChapVs = NRSVA.versesMissingChapter;
            missingVs = NRSVA.versesMissing;
            extraVs = NRSVA.extraVerses();
            /*
            Logit.WriteLine(String.Format("Versification {0}: {1} verses in missing books; {2} verses missing chapter; {3} missing verses; {4} extra verses", NRSVA.versificationName, NRSVA.booksMissing,
                NRSVA.versesMissingChapter, NRSVA.versesMissing, NRSVA.extraVerses()));
            */
            CheckVersification(LXX);
            CheckVersification(KJVA);
            CheckVersification(Catholic2);
            CheckVersification(Synodal);
            CheckVersification(Vulg);
            CheckVersification(Luther);
            CheckVersification(Catholic);
            CheckVersification(German);
            CheckVersification(NRSV);
            CheckVersification(KJV);
            CheckVersification(SynodalP);
            CheckVersification(Leningrad);
            CheckVersification(MT);
            return bestVersification;
        }

