        /// <summary>
        /// Clear the mismatch counters
        /// </summary>
        public void clear()
        {
            booksMissing = versesMissing = versesMissingChapter = 0;
        }

        public int extraVerses()
        {
            int result = 0;
            int extras;
            IDictionaryEnumerator enu = books.GetEnumerator();
            while (enu.MoveNext())
            {
                VersificationBook vbook = (VersificationBook)enu.Value;
                extras = vbook.versesPresent - vbook.versesChecked;
                if (extras > 0)
                    result += extras;
            }
            return result;
        }

        /// <summary>
        /// Checks to see if a book, chapter, and verse are present in this Sword versification
        /// </summary>
        /// <param name="book">3-character SIL book code</param>
        /// <param name="chapter"></param>
        /// <param name="verse"></param>
        /// <returns></returns>
        public bool IsVersePresent(string book, int chapter, int verse)
        {
            if (!(bookInfo.isCanon(book) || bookInfo.isApocrypha(book)))
                return false;   // Don't count this entry.
            VersificationBook vbook = (VersificationBook)books[book];
            if (vbook == null)
            {
                if (book == "ESG")
                {
                    vbook = (VersificationBook)books["EST"];
                }
                else if (book == "DAG")
                {
                    vbook = (VersificationBook)books["DAN"];
                }
                if (vbook == null)
                { 
                    booksMissing++;
                    return false;
                }
            }
            vbook.versesChecked++;
            if (chapter > vbook.versesPerChapter.Length)
            {
                versesMissingChapter++;
                versesMissing++;
                return false;
            }
            if (chapter == 0)
                return false;
            if (verse > vbook.versesPerChapter[chapter - 1])
            {
                versesMissing++;
                return false;
            }
            return true;
        }



